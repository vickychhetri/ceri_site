     Centre of Excellence for Research & Innovation (CERI) is established with a vision to initiate and foster productive research culture at CT Group of Institutions. The research practices of the institute are administered by policy concerns, by individual’s research specializations, student’s interest and projects by sponsors. To conduct new innovations and developments in changing and challenging world, there is a need to strengthen and diversify the research frontiers. Although, individuals would be free to choose their research field and continue their innovative work under the supervision of CERI and to keep the spirit with emerging challenges, CERI is always set to expand the knowledge along with team members.
     <br/>
     Through the well-established mechanism, the research activities are encouraged, appraised and promoted. The wide range of activities are implemented through CERI such as technology development, promotion of basic and applied research, refining and nurturing appropriate research skills in team members through their deputation for Supervision of PhD & Master dissertations, Projects, Conferences, Seminars, Workshops & Short Term Courses. CERI also involves in identifying new research areas, developing projects leading to publications in National/International Journals and conferences. The institute encourages the team members for publications, sponsored projects, consultancy projects and other research related activities. The management felicitates and rewards team members involved in obtaining research grants, sponsorship, consultancy projects, research publications such as papers, articles, monograph, book chapter and books etc. The stakeholders are also encouraged to file patents of the innovations which manifest to basic research and technology development. CERI guides the team members in writing projects, scientific papers leading to publication and also helps in identifying research results for filing patents. 
             
